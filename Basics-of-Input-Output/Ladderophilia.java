import java.util.*;

class Ladderophilia {
    public static void main(String args[] ) throws Exception {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        while (n -- > 0) {
            System.out.println("*   *");
            System.out.println("*   *");
            System.out.println("*****");
        }
        System.out.println("*   *");
        System.out.print("*   *");
    }
}
